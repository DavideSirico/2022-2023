STARTPGM = 0xee00
STARTDATI = 0x80
RESETVECT = 0xfffe
.area DATA (ABS)
.org STARTDATI
somma: .blkb 1
VETT:   .byte 5
	.byte 6
	.byte 7
ENDVETT:
.area PROGRAMMA (ABS)
.org STARTPGM
MAIN:
LDX #0
MOV #0,somma
LDX #VETT
LOOP:	CBEQX #ENDVETT,FINE
	LDA,X
	ADD somma
	STA somma
	INCX
	BRA LOOP
FINE : BRA FINE
.area RESET (ABS)
.org RESETVECT
.word MAIN
