STARTPGM = 0xee00
STARTDATI = 0x80
RESETVECT = 0xfffe
.area DATA (ABS)
.org STARTDATI
VETT:	.byte 5
	.byte 6
	.byte 7
ENDVETT:
.area PROGRAMMA (ABS)
.org STARTPGM
MAIN:
LDX #VETT
LDA #10
LOOP:
	STA X
	INCA
	INCX
	CBEQX #ENDVETT,FINE
	BRA LOOP
FINE : BRA FINE
.area RESET (ABS)
.org RESETVECT
.word MAIN
